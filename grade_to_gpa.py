def grade_to_GPA(grade):
    if(grade >= 90):
        return 4.0
    elif(grade >= 86):
        return 3.7
    elif(grade >= 83):
        return 3.3
    elif(grade >= 80):
        return 3.0
    elif(grade >= 76):
        return 2.7
    elif(grade >= 73):
        return 2.3
    elif(grade >= 70):
        return 2.0
    elif(grade >= 66):
        return 1.7
    elif(grade >= 63):
        return 1.3
    elif(grade >= 60):
        return 1.0
